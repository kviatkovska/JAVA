package task2;
public class Employee {
	    private String Fullname;
	    private float Salary;

	    public String getFullname() {
	        return Fullname;
	    }

	    public void setFullname(String fullname) {
	        Fullname = fullname;
	    }

	    public float getSalary() {
	        return Salary;
	    }

	    public void setSalary(float salary) {
	        Salary = salary;
	}
}
